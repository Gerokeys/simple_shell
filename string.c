/**
 * _strlen - returns the length of a stringerty
 * @s: the string whose length to checkety
 *
 * Return: integer length of string
 */
int _strlen(char *s)
{
	int i = 0;

	if (!s)
		return (0);

	while (*s++)
		i++;
	return (i);
}

